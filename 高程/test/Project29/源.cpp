/*
【问题描述】
编写一个程序,读入一个以字符串表示的非负整数(串中每个字符代表一个数字),
输出该正整数加一后的结果。
【输入形式】
输入一行为正整数字符串,该正整数最前不包含字符'0'(除非该正整数等于0),
正整数字符串的长度小于50(即正整数的大小小于10^50)
【输出形式】
输出一行为加一后的正整数字符串
*/

#include <iostream>
#include <string>
using namespace std;

int main() {
    string num;        // 存储输入的数字字符串
    cin >> num;        // 读取输入

    int carry = 1;     // 进位标志,初始为1(因为要加1)

    // 从字符串的最后一位(个位)开始向前遍历
    // 条件: i >= 0(没遍历完) && carry(还有进位需要处理)
    for (int i = num.length() - 1; i >= 0 && carry; i--) {
        // 将当前字符转换为数字,并加上进位
        // num[i] - '0': 字符转数字,例如 '5' - '0' = 5
        int digit = num[i] - '0' + carry;

        // 更新当前位的数字
        // digit % 10: 取个位数字(处理进位后的结果)
        // + '0': 将数字转回字符,例如 5 + '0' = '5'
        num[i] = (digit % 10) + '0';

        // 计算新的进位
        // digit / 10: 如果digit >= 10,进位为1;否则为0
        carry = digit / 10;
    }

    // 如果循环结束后还有进位(例如 999 + 1 = 1000)
    if (carry) {
        num = "1" + num;   // 在字符串最前面加上'1'
    }

    cout << num << endl;   // 输出结果

    return 0;
}