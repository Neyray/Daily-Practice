/*
【问题描述】
编写一函数expand(s1,s2)，用以将字符串s1中的缩记符号在字符串s2中扩展为等价的完整字符，
例如将a-d扩展为abcd。该函数可以处理大小写字母和数字，并可以处理a-b-c、a-z0-9与a-z等类似的情况。
在main函数中测试该函数：从键盘输入包含缩记符号的字符串，然后调用该函数进行扩展，输出扩展结果。

注意：
1.待扩展字符串中有可能包含空格，例如：a-d x-z应扩展成：abcd xyz。所以读入待扩展字符串时，应能够
  读入包含空格的字符串。
2.只要缩记符号-之后的字符比之前的字符的ASCII码值大，就要将它们之间的所有字符扩展出来，例如：Z-
  a之间的字符也要扩展出来；
3.特殊情况：a-b-c将被扩展为：abc。a-a将被扩展为：a-a。

【输入形式】
从键盘输入包含扩展符的字符串

【输出形式】
输出扩展后的字符串
*/

#include <iostream>
#include <string>
using namespace std;

// 字符串扩展函数：将缩写形式如"a-d"扩展为"abcd"
string expand(const string& s1) {
    string s2 = "";  // 用于存储扩展后的结果字符串
    int i = 0;  // 遍历输入字符串的索引

    // 遍历输入字符串s1的每个字符
    while (i < s1.length()) {
        // 检查是否存在缩写模式：当前字符-下一个字符
        // 条件：i+2不越界 且 第i+1个字符是'-'
        if (i + 2 < s1.length() && s1[i + 1] == '-') {
            char start = s1[i];      // 获取起始字符
            char end = s1[i + 2];    // 获取结束字符

            // 判断起始字符的ASCII码是否小于结束字符
            // 只有start < end时才进行扩展
            if (start < end) {
                // 从起始字符扩展到结束字符（包括两端）
                for (char c = start; c <= end; c++) {
                    s2 += c;  // 将每个字符添加到结果字符串
                }
                i += 3;  // 跳过已处理的三个字符（start、-、end）
            }
            else {
                // 如果start >= end，不进行扩展，只添加当前字符
                // 例如："a-a"保持为"a-a"
                s2 += s1[i];
                i++;  // 移动到下一个字符
            }
        }
        else {
            // 如果不是缩写模式，直接将当前字符添加到结果中
            // 包括普通字符、空格等
            s2 += s1[i];
            i++;  // 移动到下一个字符
        }
    }

    return s2;  // 返回扩展后的字符串
}

int main() {
    string s1;  // 存储输入的待扩展字符串

    // 使用getline读取一行输入（包括空格）
    getline(cin, s1);

    // 调用expand函数进行字符串扩展
    string s2 = expand(s1);

    // 输出扩展后的结果
    cout << s2 << endl;

    return 0;
}