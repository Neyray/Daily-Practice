/*【问题描述】

 对于一个长度为n的字符串 s=s0s1⋯sn−1来说，子串的定义是从中选出两个下标l,r(0≤l≤r≤n−1)，这之间所有的字符组合起来的一个新的字符串：s′=slsl+1⋯sr 就是其中一个子串。给出一个只有数字字符0∼9组成的数字字符串，输出子串是好串的数目。一个子串是好串，当且仅当它满足以下两个条件之一：

1、单字符子串一定是好串，即当子串长度为1时，它总是好串；

2、长度大于1时，可以拆分为两个单字符子串、一个单字符子串和一个连续非递减子串或者两个连续非递减子串，一个串p=p0p1…pk−1 为连续非递减子串是指：对于所有1≤i<k，满足pi=pi−1或 pi=pi−1+1，即数字串中的每一个数字，要么等于上一个数字，要么等于上一个数字加1，例如122、456是连续非递减子串。例如：12258的好串个数为12，分别为：1、2、2、5、8、12、22、25、58、122、225、1225；18967的好串个数为13，分别为：1、8、9、6、7、18、89、96、67、189、896、967、8967。

【输入形式】从键盘输入一行数字字符串S。

【输出形式】在屏幕上输出S的好串数目。

【样例输入】18967

【样例输出】13

【评分标准】该题目有10个测试用例，每通过一个测试用例，得10分。

【样例说明】

12258的好串个数为12，

长度为1的好串：1、2、2、5、8

长度为2的好串：12、22、25、58

长度为3的好串：122、225

长度为4的好串：1225

18967的好串个数为13，

长度为1的好串：1、8、9、6、7

长度为2的好串：18、89、96、67

长度为3的好串：189、896、967

长度为4的好串：8967
*/
#include <iostream>
#include <string>
#include <vector>
using namespace std;

int main() {
    string s;
    cin >> s;
    int n = s.size();

    //i代表起始指针，j代表尾指针
    vector<vector<bool>> valid(n, vector<bool>(n, false));
    int count = 0;

    // 预处理连续非递减子串
    for (int i = 0; i < n; ++i) {
        valid[i][i] = true;
        for (int j = i + 1; j < n; ++j) {
            if (valid[i][j - 1] && (s[j] == s[j - 1] || s[j] == s[j - 1] + 1)) {
                valid[i][j] = true;
            }
        }
    }

    // 初始化单字符子串
    for (int i = 0; i < n; ++i) {
        count++;
    }

    // 处理长度≥2的子串
    for (int len = 2; len <= n; ++len) {
        for (int i = 0; i <= n - len; ++i) {
            int j = i + len - 1;

            // 如果子串本身是连续非递减，直接标记为好子串[1]
            if (valid[i][j]) {
                count++;
                continue;
            }

            // 遍历所有可能的拆分点k
            bool found = false;
            for (int k = i; k < j; ++k) {
                // 判断左半部分是否满足条件（单字符或连续非递减）[3,4]
                bool left_ok = (k - i + 1 == 1) ? true : valid[i][k];
                // 判断右半部分是否满足条件（单字符或连续非递减）[3,4]
                bool right_ok = (j - (k + 1) + 1 == 1) ? true : valid[k + 1][j];
                if (left_ok && right_ok) {
                    found = true;
                    break;
                }
            }

            if (found) {
                count++;
            }
        }
    }

    cout << count << endl;
    return 0;
}