// 魹ｽ魹ｽ魹ｽ魹ｽﾊｽ魹ｽ魹ｽ魹ｽﾚｵ羝ｨ魹ｽ魹ｽ
struct TreeNode {
    char val;
    TreeNode* left;
    TreeNode* right;
    TreeNode(char c) : val(c), left(nullptr), right(nullptr) {}
};

// 魹ｽ魹ｽﾈ｡魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾈｼ魹ｽ
int precedence(char op) {
    if (op == '+' || op == '-') return 1;
    if (op == '*' || op == '/') return 2;
    return 0;
}

// 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｽ魹ｽ魹ｽ
TreeNode* buildExpressionTree(const string& expr) {
    stack<TreeNode*> nodes;
    stack<char> ops;

    for (size_t i = 0; i < expr.length(); ++i) {
        char c = expr[i];
        if (c == ' ') continue;
        if (isalnum(c)) {
            nodes.push(new TreeNode(c));
        }
        else if (c == '(') {
            ops.push(c);
        }
        else if (c == ')') {
            while (!ops.empty() && ops.top() != '(') {
                TreeNode* right = nodes.top(); nodes.pop();
                TreeNode* left = nodes.top(); nodes.pop();
                TreeNode* opNode = new TreeNode(ops.top()); ops.pop();
                opNode->left = left;
                opNode->right = right;
                nodes.push(opNode);
            }
            if (!ops.empty()) ops.pop(); // 魹ｽ魹ｽ魹ｽ魹ｽ '('
        }
        else {
            while (!ops.empty() && precedence(ops.top()) >= precedence(c)) {
                TreeNode* right = nodes.top(); nodes.pop();
                TreeNode* left = nodes.top(); nodes.pop();
                TreeNode* opNode = new TreeNode(ops.top()); ops.pop();
                opNode->left = left;
                opNode->right = right;
                nodes.push(opNode);
            }
            ops.push(c);
        }
    }

    while (!ops.empty()) {
        TreeNode* right = nodes.top(); nodes.pop();
        TreeNode* left = nodes.top(); nodes.pop();
        TreeNode* opNode = new TreeNode(ops.top()); ops.pop();
        opNode->left = left;
        opNode->right = right;
        nodes.push(opNode);
    }

    return nodes.top();
}

// 魹ｽ魹ｽﾈ｡魹ｽﾖｷ魹ｽ魹ｽ魹ｽﾓｦﾖｵ魹ｽ魹ｽa/A=1, ..., z/Z=26魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｱ魹ｽﾓｷ魹ｽ魹ｽ魹ｽ
int getValue(char c) {
    if (isdigit(c)) return c - '0';
    if (isalpha(c)) return toupper(c) - 'A' + 1;
    return 0;
}

// 魹ｽ魹ｽ魹ｽ魹ｽﾊｽ魹ｽ魹ｽ魹ｽ魹ｽﾖｵ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
int evaluate(TreeNode* root) {
    if (!root) return 0;

    // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾒｶ魹ｽﾓｽﾚｵ罐ｨ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    if (!root->left && !root->right) {
        return getValue(root->val);
    }

    // 魹ｽﾝｹ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    int leftVal = evaluate(root->left);
    int rightVal = evaluate(root->right);

    // 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
    switch (root->val) {
    case '+': return leftVal + rightVal;
    case '-': return leftVal - rightVal;
    case '*': return leftVal * rightVal;
    case '/': return rightVal != 0 ? leftVal / rightVal : 0;
    }

    return 0;
}


// 魹ｽ魹ｽﾎｱ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ
void levelOrderReverse(TreeNode* root, string& out) {
    if (!root) return;
    queue<TreeNode*> q;
    vector<char> result;
    q.push(root);
    while (!q.empty()) {
        TreeNode* node = q.front(); q.pop();
        result.push_back(node->val);
        if (node->left) q.push(node->left);
        if (node->right) q.push(node->right);
    }
    for (auto it = result.rbegin(); it != result.rend(); ++it) {
        out += *it;
    }
}

// 魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾉｺﾏｷ魹ｽ魹ｽ魹ｽﾗｺ魹ｽ魹ｽ魹ｽ魹ｽﾊｽ
void postorder(TreeNode* root, string& postfix) {
    if (!root) return;
    postorder(root->left, postfix);
    postorder(root->right, postfix);
    postfix += root->val;
}

// 魹ｽ魹ｽ魹ｽ豐ｨ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽ魹ｽﾊｽ魹ｽ魹ｽﾖｵ
int evaluatePostfix(const string& expr) {
    stack<int> s;
    for (char c : expr) {
        if (isalnum(c)) {
            s.push(getValue(c));
        }
        else {
            int right = s.top(); s.pop();
            int left = s.top(); s.pop();
            int res = 0;
            switch (c) {
            case '+': res = left + right; break;
            case '-': res = left - right; break;
            case '*': res = left * right; break;
            case '/': res = right != 0 ? left / right : 0; break;
            }
            s.push(res);
        }
    }
    return s.top();
}

